public class Date
{
    private int month, day;
    public Date(int month, int day)
    {
        this.month = month;
        this.day = day;
    }
    public void setMonth(int month)
    {
        this.month = month;
    }
    public void setDay(int day)
    {
        this.day = day;
    }
    public int getMonth()
    {
        return month;
    }
    public int getDay()
    {
        return day;
    }

}